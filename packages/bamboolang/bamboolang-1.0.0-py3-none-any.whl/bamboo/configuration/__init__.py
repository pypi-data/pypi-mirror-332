# ProcNum  is the maximal Proc num
# Backend is "x86" or "sw"
@dataclass
class DSLConfiguration:
    def __init__(
        self,
        Grid: GridConfiguration,
        ProcNum: int,
        Backend: BackendDesc,
        Profiling: bool = False,
    ):
        self.Grid = Grid
        self.ProcNum = ProcNum
        self.Backend = Backend
        self.Profiling = Profiling
        self.LonHaloPreLat: List[int] = []

    def __str__(self) -> str:
        return f"{self.Grid}, ProcNum is {self.ProcNum}, Backend is {self.Backend}"


