# User Define Grid Configure
# GridType is "LonLat" or "CubedSphere"
@dataclass
class GridConfiguration:
    def __init__(
        self,
        GridType: str,
        NumLon: int,
        NumLat: int,
        NumLev: int,
        CornerHalo: int = 2,
        ExternHalo: Tuple[int, int, int] = [0, 0, 0],
        MemberNum: int = 1,
    ):
        self.GridType = GridType
        self.NumLon = NumLon
        self.NumLat = NumLat
        self.NumLev = NumLev
        self.CornerHalo = CornerHalo
        self.ExternHalo = ExternHalo
        self.MemberNum = MemberNum

    def __str__(self) -> str:
        return f"GridType is {self.GridType}, resolution is {self.NumLon}*{self.NumLat}*{self.NumLev}, MemberNum is {self.MemberNum}"


