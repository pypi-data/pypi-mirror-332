class rumusMatRev:
    def persegi(x):
        return x*x
    
    def luasPersegiPanjang(x, y):
        return x*y
    
    def tambah (x, y):
        return x+y
    
    def kurang (x, y):
        return x-y
    
    def kali (x, y):
        return x*y
    
    def bagi (x, y):
        return x/y
    
